-- PAYMENT AGGREGATE (To prevent duplicate rows in order facts)
CREATE OR REPLACE TABLE SILVER.FACT_ORDER_ITEMS AS
WITH PAYMENT_AGG AS (
    SELECT
        ORDER_ID,
        SUM(PAYMENT_VALUE) AS TOTAL_PAYMENT_VALUE,
        MAX(PAYMENT_INSTALLMENTS) AS PAYMENT_INSTALLMENTS
    FROM BRONZE.BRONZE_PAYMENTS
    GROUP BY ORDER_ID
),

REVIEW_AGG AS (
    SELECT
        ORDER_ID,
        AVG(REVIEW_SCORE) AS REVIEW_SCORE
    FROM BRONZE.BRONZE_REVIEWS
    GROUP BY ORDER_ID
)

SELECT
    O.ORDER_ID,
    DC.CUSTOMER_KEY,
    DP.PRODUCT_KEY,
    DS.SELLER_KEY,

    O.ORDER_PURCHASE_TS,
    O.ORDER_APPROVED_TS,
    O.ORDER_DELIVERED_TS,
    O.ORDER_STATUS,

    OI.ORDER_ITEM_ID,
    OI.PRICE,
    OI.FREIGHT_VALUE,

    P.TOTAL_PAYMENT_VALUE,
    P.PAYMENT_INSTALLMENTS,

    R.REVIEW_SCORE

FROM BRONZE.BRONZE_ORDER_ITEMS OI

INNER JOIN BRONZE.BRONZE_ORDERS O
    ON OI.ORDER_ID = O.ORDER_ID

INNER JOIN SILVER.DIM_CUSTOMER DC
    ON O.CUSTOMER_ID = DC.CUSTOMER_ID

LEFT JOIN SILVER.DIM_PRODUCT DP
    ON OI.PRODUCT_ID = DP.PRODUCT_ID

LEFT JOIN SILVER.DIM_SELLER DS
    ON OI.SELLER_ID = DS.SELLER_ID

LEFT JOIN PAYMENT_AGG P
    ON O.ORDER_ID = P.ORDER_ID

LEFT JOIN REVIEW_AGG R
    ON O.ORDER_ID = R.ORDER_ID;


