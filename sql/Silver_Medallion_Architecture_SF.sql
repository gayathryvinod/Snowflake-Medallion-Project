-- CREATE FACT AND DIMENSION TABLES 

-- CUSTOMER (DIMENSION)
CREATE OR REPLACE TABLE SILVER.DIM_CUSTOMER AS
SELECT
    ROW_NUMBER() OVER (ORDER BY CUSTOMER_ID) AS CUSTOMER_KEY,
    CUSTOMER_ID,
    CUSTOMER_UNIQUE_ID,
    CUSTOMER_CITY,
    CUSTOMER_STATE
FROM BRONZE.BRONZE_CUSTOMERS;

SELECT * FROM SILVER.DIM_CUSTOMER;
SELECT COUNT(DISTINCT CUSTOMER_ID) FROM SILVER.DIM_CUSTOMER;

-- PRODUCT DIMENSION TABLE
CREATE OR REPLACE TABLE SILVER.DIM_PRODUCT AS
SELECT
    ROW_NUMBER() OVER (ORDER BY P.PRODUCT_ID) AS PRODUCT_KEY,
    P.PRODUCT_ID,
    P.PRODUCT_CATEGORY_NAME,
    T.PRODUCT_CATEGORY_NAME_ENGLISH,
    P.PRODUCT_NAME_LENGTH,
    P.PRODUCT_DESCRIPTION_LENGTH,
    P.PRODUCT_PHOTOS_QTY,
    P.PRODUCT_WEIGHT_G,
    P.PRODUCT_LENGTH_CM,
    P.PRODUCT_HEIGHT_CM,
    P.PRODUCT_WIDTH_CM
FROM BRONZE.BRONZE_PRODUCTS P
LEFT JOIN BRONZE.BRONZE_PRODUCT_CATEGORY_TRANSLATION T
ON P.PRODUCT_CATEGORY_NAME = T.PRODUCT_CATEGORY_NAME;

-- SELLER DIMENSION TABLE 
CREATE OR REPLACE TABLE SILVER.DIM_SELLER AS
SELECT
    ROW_NUMBER() OVER (ORDER BY SELLER_ID) AS SELLER_KEY,
    SELLER_ID,
    SELLER_CITY,
    SELLER_STATE
FROM BRONZE.BRONZE_SELLERS;

